package com.abdullahsoubeih.usingretrofitwihsql.RetrofitWithListView.Classes;


//this is ModelClass
public class ItemForListView {

   // @SerializedName("name")
    private String name,realname,team,firstappearance,createdby,publisher,imageurl,bio;

//constructor
    public ItemForListView(String name, String realname, String team, String firstappearance, String createdby, String publisher, String imageurl, String bio) {
        this.name = name;
        this.realname = realname;
        this.team = team;
        this.firstappearance = firstappearance;
        this.createdby = createdby;
        this.publisher = publisher;
        this.imageurl = imageurl;
        this.bio = bio;
    }


    //getter
    public String getName() {
        return name;
    }

    public String getRealname() {
        return realname;
    }

    public String getTeam() {
        return team;
    }

    public String getFirstappearance() {
        return firstappearance;
    }

    public String getCreatedby() {
        return createdby;
    }

    public String getPublisher() {
        return publisher;
    }

    public String getImageurl() {
        return imageurl;
    }

    public String getBio() {
        return bio;
    }



}


